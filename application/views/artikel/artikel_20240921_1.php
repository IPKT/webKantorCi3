    <p><strong>Kejadian dan Parameter Gempabumi:</strong> <br>
        Sabtu, 21 September 2024 pukul 07.26.20 WITA, wilayah Gianyar diguncang gempabumi tektonik. Hasil analisa BMKG
        menunjukkan bahwa gempabumi ini berkekuatan M=4,8. Episenter terletak pada koordinat 8,57° LS; 115,32° BT, atau
        tepatnya berlokasi di darat pada jarak 3 km barat daya Gianyar, Bali pada kedalaman 22 km. <br> <br>

        <strong>Jenis dan Mekanisme Gempabumi:</strong> <br>
        Dengan memperhatikan lokasi episenter dan kedalaman hiposenternya, gempabumi yang terjadi merupakan jenis
        gempabumi dangkal akibat aktivitas sesar aktif di darat. Hasil analisis mekanisme sumber menunjukkan bahwa
        gempabumi memiliki mekanisme sesar turun dengan kombinasi mendatar ( Normal Oblique ) <br> <br>

        <strong>Dampak Gempabumi:</strong> <br>
        Dampak gempabumi berdasarkan laporan masyarakat berupa guncangan dirasakan di wilayah Gianyar IV MMI (Pada siang
        hari dirasakan oleh orang banyak dalam rumah, di luar oleh beberapa orang, gerabah pecah, jendela/pintu berderik
        dan dinding berbunyi), Badung, Denpasar, Tabanan, Karangasem dan Bangli III MMI (Getaran dirasakan nyata dalam
        rumah. Terasa getaran seakan-akan ada truk berlalu), Buleleng, Mataram dan Lombok Barat II MMI (Getaran
        dirasakan oleh beberapa orang, benda-benda ringan yang digantung bergoyang) . Hingga saat ini belum ada laporan
        dampak kerusakan yang ditimbulkan akibat gempabumi tersebut. Hasil pemodelan tsunami dengan sumber gempabumi
        tektonik menunjukkan bahwa gempabumi ini TIDAK BERPOTENSI TSUNAMI. <br><br>

        <strong>Gempabumi Susulan:</strong> <br>
        Hingga hari Sabtu, 21 September 2024 pukul 07.42 WITA, hasil monitoring BMKG menunjukkan adanya 1 aktivitas
        gempabumi susulan ( aftershock ). <br> <br>

        <strong>Rekomendasi:</strong> <br>
        Kepada Masyarakat dihimbau agar tetap tenang dan tidak terpengaruh oleh isu yang tidak dapat
        dipertanggungjawabkan kebenaranya. Agar menghindari bangunan yang retak atau rusak diakibatkan oleh gempa.
        Periksa dan pastikan bangunan tempat tinggal anda cukup tahan gempa, ataupun tidak ada kerusakan akibat getaran
        gempa yang membahayakan kestabilan bangunan sebelum anda kembali kedalam rumah. <br>

